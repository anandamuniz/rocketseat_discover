//OBJETOS -  é uma coleção de propriedades, e uma propriedade é uma associação entre um nome (ou chave) e um valor.
console.log({
    nome: "Ananda",
    idade: 29
})

//ARRAY - agrupamento de dados (lista)
console.log([
    "Ananda",
    29
])

//OBJET
const person = { name: 'John', age: 20 }
console.log(person.name)            //para acessar a propriedade name

//ARRAY
const animals = [
    'leão',
    'macaco',
    'cachorro'
]

console.log(animals[1])         //para acessar valores dentro do array
console.log(animals.length)     //para determinar o tamanho do array